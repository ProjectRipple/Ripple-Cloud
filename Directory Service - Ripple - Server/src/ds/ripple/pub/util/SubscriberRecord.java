package ds.ripple.pub.util;

public class SubscriberRecord {
	private int sub_Id;
	private int sub_name;

	public int getPub_Id() {
		return sub_Id;
	}

	public void setPub_Id(int sub_Id) {
		this.sub_Id = sub_Id;
	}

	public int getPub_name() {
		return sub_name;
	}

	public void setPub_name(int sub_name) {
		this.sub_name = sub_name;
	}
}
